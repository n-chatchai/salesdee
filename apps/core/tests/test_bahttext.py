import pytest

from apps.core.utils.bahttext import baht_text


@pytest.mark.parametrize(
    ("amount", "expected"),
    [
        ("0", "ศูนย์บาทถ้วน"),
        ("0.00", "ศูนย์บาทถ้วน"),
        ("1", "หนึ่งบาทถ้วน"),
        ("10", "สิบบาทถ้วน"),
        ("11", "สิบเอ็ดบาทถ้วน"),
        ("20", "ยี่สิบบาทถ้วน"),
        ("21", "ยี่สิบเอ็ดบาทถ้วน"),
        ("100", "หนึ่งร้อยบาทถ้วน"),
        ("101", "หนึ่งร้อยเอ็ดบาทถ้วน"),
        ("1000", "หนึ่งพันบาทถ้วน"),
        ("12034", "หนึ่งหมื่นสองพันสามสิบสี่บาทถ้วน"),
        ("1000000", "หนึ่งล้านบาทถ้วน"),
        ("1000001", "หนึ่งล้านเอ็ดบาทถ้วน"),
        ("1234567", "หนึ่งล้านสองแสนสามหมื่นสี่พันห้าร้อยหกสิบเจ็ดบาทถ้วน"),
        ("0.50", "ห้าสิบสตางค์"),
        ("0.01", "หนึ่งสตางค์"),
        ("0.25", "ยี่สิบห้าสตางค์"),
        ("100.50", "หนึ่งร้อยบาทห้าสิบสตางค์"),
        ("12034.50", "หนึ่งหมื่นสองพันสามสิบสี่บาทห้าสิบสตางค์"),
        ("1234567.89", "หนึ่งล้านสองแสนสามหมื่นสี่พันห้าร้อยหกสิบเจ็ดบาทแปดสิบเก้าสตางค์"),
    ],
)
def test_baht_text(amount: str, expected: str) -> None:
    assert baht_text(amount) == expected


def test_baht_text_rounds_half_up() -> None:
    assert baht_text("0.005") == "หนึ่งสตางค์"


def test_baht_text_negative() -> None:
    assert baht_text("-5") == "ลบห้าบาทถ้วน"
